<p>《西游记》一部伟大的作品，中国四大名著之一，拥有广泛的民众基础和粉丝，可以说是家喻户晓。</p>
<p>近年来围绕《西游记》进行改编或二次创作的作品，越来越多，经典电影《大话西游》，热门游戏《梦幻西游》，长篇穿越小说《重生西游之天篷妖尊》都是优秀的西游题材作品，那到底还有多少与西游记有关的作品呢？</p>
<p>作为一名真正的西游记爱好者，不会只是肤浅地告诉你还有这个还有那个，我们选择了建设一个网站，用一辈子去收集西游题材，只要热爱西游的人还有，只要还有人创作出新作品，我们就会一直更新下去。</p>
<p>无论是电视电影，还是小说，亦或游戏，只要是与西游有关的，我们都会进行收录！</p>